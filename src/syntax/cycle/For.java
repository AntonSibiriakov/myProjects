package syntax.cycle;

public class For {

    public static void main(String[] args){
        //for(<начальная точка>; <условие выхода>; <операторы счетчика>) {
        // Тело цикла
        //Пример перебора цифр от 0 до 5 и вывод каждой в консоль:
        for(int i = 0; i < 5; i++) {
            System.out.println(i);    }
    }
}